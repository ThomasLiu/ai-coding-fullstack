#!/bin/bash
# generate-skill.sh - Generate skill from effort classification
# Creates a skill file with appropriate effort frontmatter

set -e

usage() {
    cat << EOF
Usage: $(basename "$0") [OPTIONS]

Generate a skill file with effort frontmatter.

OPTIONS:
    --name <name>       Skill name (required)
    --effort <level>    Effort level: low, medium, high (required)
    --output <dir>      Output directory (default: current dir)
    --description <text> Skill description
    --help              Show this help message

EXAMPLES:
    $(basename "$0") --name "auth-skill" --effort medium
    $(basename "$0") --name "quick-fix" --effort low --output ./skills

EOF
}

main() {
    local name=""
    local effort="medium"
    local output_dir="."
    local description=""

    while [[ $# -gt 0 ]]; do
        case "$1" in
            --name)
                name="$2"
                shift 2
                ;;
            --effort)
                effort="$2"
                shift 2
                ;;
            --output)
                output_dir="$2"
                shift 2
                ;;
            --description)
                description="$2"
                shift 2
                ;;
            --help)
                usage
                exit 0
                ;;
            *)
                echo "Unknown option: $1" >&2
                usage >&2
                exit 1
                ;;
        esac
    done

    if [[ -z "$name" ]]; then
        echo "Error: --name is required" >&2
        usage >&2
        exit 1
    fi

    if [[ ! "$effort" =~ ^(low|medium|high)$ ]]; then
        echo "Error: --effort must be low, medium, or high" >&2
        exit 1
    fi

    # Create skill directory
    local skill_dir="$output_dir/$name"
    mkdir -p "$skill_dir"

    # Generate SKILL.md
    cat > "$skill_dir/SKILL.md" << EOF
# $name

## Metadata
- **effort**: $effort
- **created**: $(date +%Y-%m-%d)

## Description
${description:-Auto-generated skill with $effort effort level.}

## Usage

\`\`\`bash
# Use this skill for task execution
\`\`\`

## Integration

This skill was generated by the effort-classifier system.
EOF

    echo "✅ Created skill: $skill_dir/SKILL.md (effort: $effort)"
}

main "$@"
